
public class Carta {
    String nomeCarta;
    String naipeCarta;

    public Carta(String nome, String naipe) {
        this.nomeCarta = nome;
        this.naipeCarta = naipe;
    }

    public String getNomeCarta() {
        return nomeCarta;
    }
    public String getNaipeCarta() {
        return naipeCarta;
    }

}

